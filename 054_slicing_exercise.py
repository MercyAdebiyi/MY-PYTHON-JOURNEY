#   Given the string text = "CyberSecurity", write a slice that returns "Cyber" and another slice that returns "Security".

print("\n**************EXERSICE 1*******************""\n")

course="CyberSecurity"
print(f"{course[0:5]}")
print(f"{course[5:13]}")



print("\n**************STRING REVERSE*******************""\n")
print(course[::-1])


